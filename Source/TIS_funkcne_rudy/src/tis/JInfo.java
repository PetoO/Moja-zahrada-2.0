    /**
     * pri rozkliknutí obrázku vybehne ponuka na nastavenie nového obrázku, ktorý sa následne uloží
     * @param evt 
     */
    private void JObrazokMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JObrazokMouseClicked
        if (evt.getClickCount() == 2) {       
            int returnVal = fc.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                String content = fc.getSelectedFile().toString();
                ImageIcon icon = new ImageIcon(content);
                JObrazok.setIcon(icon); 
                
		FileOutputStream fop = null;
		File file;
 
		try {
 
			file = new File("OBR.dat");
			fop = new FileOutputStream(file);
 
			// if file doesnt exists, then create it
			if (!file.exists()) {
				file.createNewFile();
			}
 
			// get the content in bytes
			byte[] contentInBytes = content.getBytes();
			byte[] menoInBytes = meno.getBytes();
 
			fop.write(menoInBytes);
                        fop.write(System.getProperty("line.separator").getBytes());
			fop.write(contentInBytes);
			fop.flush();
			fop.close();
 
			System.out.println("Done");
 
		} catch (IOException e) {
			e.printStackTrace();
		} finally {
			try {
				if (fop != null) {
					fop.close();
				}
			} catch (IOException e) {
				e.printStackTrace();
			}
		}                
            }
        }
    }//GEN-LAST:event_JObrazokMouseClicked

