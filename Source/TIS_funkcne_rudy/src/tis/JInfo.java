    /**
     * nastaví do labalov informácie o prvku (ktoré pochádzajú z excelovskej tabulky)
     * tiež načíta iný zvolený obŕazok ak taký je
     * @param pole 
     */
    public void setInfo(ArrayList<String> pole) {  
        if (pole.get(0)!= null) {
            meno = pole.get(0);
            jLabel1.setText(Character.toString(meno.charAt(0)).toUpperCase()+meno.substring(1));
        }
        if (pole.get(1)!= null) jLabel2.setText(pole.get(1));
        if (pole.get(2)!= null) jLabel3.setText(pole.get(2));
        if (pole.get(3)!= null) jLabel4.setText(pole.get(3)+" m");
        if (pole.get(4)!= null) jLabel5.setText(pole.get(4));
        if (pole.get(5)!= null) jLabel6.setText(pole.get(5));
        if (pole.get(6)!= null) jLabel7.setText(pole.get(6));
        if (pole.get(7)!= null) jLabel8.setText(pole.get(7));
        if (pole.get(8)!= null) jLabel9.setText(pole.get(8));
        if (pole.get(9)!= null) jLabel10.setText(pole.get(9));
        if (pole.get(10)!= null) jLabel11.setText(pole.get(10));
        if (pole.get(11)!= null) jLabel12.setText(pole.get(11));
        
        
        
        
        String str = "";
        BufferedReader buffread;
        ImageIcon icon = new ImageIcon();
        try {
            buffread = new BufferedReader(new FileReader(new File("OBR.dat")));
            String line;
            while ((line = buffread.readLine()) != null) {
            str = line;
            if (str.equals(meno)) {
                icon = new ImageIcon(buffread.readLine()); 
                if (icon != null)
                    JObrazok.setIcon(icon);
                }
            }
        
        
            buffread.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JInfo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
